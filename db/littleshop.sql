-- View: dicCurrencyList
DROP VIEW IF EXISTS dicCurrencyList;
CREATE VIEW IF NOT EXISTS dicCurrencyList AS
    SELECT Currency.id,
           Currency.short,
           Currency.fullname,
           LastT.rate,
           LastT.multiplicity
      FROM riCurrencyRate AS LastT
           INNER JOIN
           (
               SELECT MAX([Last].Period) AS Period,
                      [Last].currency_id AS Id
                 FROM riCurrencyRate AS [Last]
                GROUP BY [Last].currency_id
           )
           AS LastD ON LastT.currency_id = LastD.Id AND 
                       LastT.Period = LastD.Period
           INNER JOIN
           dicCurrency AS Currency ON LastT.currency_id = Currency.Id;


